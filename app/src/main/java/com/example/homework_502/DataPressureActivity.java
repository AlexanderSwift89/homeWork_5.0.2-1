package com.example.homework_502;

public class DataPressureActivity {

    private String upPressure;
    private String lowPressure;
    private String pulse;

    public String getUpPressure() {
        return upPressure;
    }

    public void setUpPressure(String upPressure) {
        this.upPressure = upPressure;
    }

    public String getLowPressure() {
        return lowPressure;
    }

    public void setLowPressure(String lowPressure) {
        this.lowPressure = lowPressure;
    }

    public String getPulse() {
        return pulse;
    }

    public void setPulse(String pulse) {
        this.pulse = pulse;
    }
}
