package com.example.homework_502;

public class DataMainActivity {
    private String inputName;
    private String inputSurname;
    private String inputSecondName;
    private String inputAge;

    public String getName() {
        return inputName;
    }

    public void setName(String inputName) {
        this.inputName = inputName;
    }

    public String getSurname() {
        return inputSurname;
    }

    public void setSurname(String inputSurname) {
        this.inputSurname = inputSurname;
    }

    public String getSecondName() {
        return inputSecondName;
    }

    public void setSecondName(String inputSecondName) {
        this.inputSecondName = inputSecondName;
    }

    public String getAge() {
        return inputAge;
    }

    public void setAge(String inputAge) {
        this.inputAge = inputAge;
    }
}
